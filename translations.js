/**
 * FunГ§ГЈo para obter texto traduzido
 * @param {string} key - Chave da traduГ§ГЈo
 * @returns {string} Texto traduzido
 */
function t(key) {
    return translations[currentLanguage][key] || translations['en'][key] || key;
}

/**
 * FunГ§ГЈo para alterar idioma
 * @param {string} languageCode - CГіdigo do idioma
 */
function changeLanguage(languageCode) {
    if (translations[languageCode]) {
        currentLanguage = languageCode;
        localStorage.setItem('chomageLang', languageCode);
        updateInterface();
        
        // Recalcula se jГЎ hГЎ resultados
        const resultsDiv = document.getElementById('results');
        if (resultsDiv && resultsDiv.style.display !== 'none') {
            calculateBenefits();
        }
    }
}

/**
 * FunГ§ГЈo para carregar idioma salvo
 */
function loadSavedLanguage() {
    const savedLang = localStorage.getItem('chomageLang');
    if (savedLang && translations[savedLang]) {
        currentLanguage = savedLang;
    }
}

/**
 * FunГ§ГЈo para atualizar toda a interface
 */
function updateInterface() {
    // Atualizar tГӯtulo da pГЎgina
    document.title = t('title');
    
    // Atualizar elementos com data-i18n
    const elementsToTranslate = document.querySelectorAll('[data-i18n]');
    elementsToTranslate.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (element.tagName === 'INPUT' && (element.type === 'text' || element.type === 'number')) {
            if (key.includes('Placeholder')) {
                element.placeholder = t(key);
            }
        } else {
            element.textContent = t(key);
        }
    });
    
    // Atualizar o seletor de idioma
    const languageSelect = document.getElementById('language-select');
    if (languageSelect) {
        languageSelect.value = currentLanguage;
    }
    
    console.log(`Interface atualizada para: ${currentLanguage}`);
}

/**
 * Inicializar sistema de idiomas
 */
function initializeI18n() {
    loadSavedLanguage();
    populateLanguageSelector();
    updateInterface();
}

/**
 * Popular o seletor de idiomas que jГЎ existe no HTML
 */
function populateLanguageSelector() {
    const languageSelect = document.getElementById('language-select');
    if (languageSelect) {
        languageSelect.innerHTML = availableLanguages.map(lang => 
            `<option value="${lang.code}" ${lang.code === currentLanguage ? 'selected' : ''}>
                ${lang.flag} ${lang.name}
            </option>`
        ).join('');
    }
}
